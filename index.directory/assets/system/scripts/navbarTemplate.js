/**
 * Navbar Template Module (JavaScript version)
 * Provides a consistent navbar script tag for all pages
 * 
 * This is a direct JavaScript port of .github/scripts/navbar_template.py
 */

/**
 * Generate navbar HTML comment
 * The navbar is now generated dynamically by navbar.js
 * 
 * @param {string} level - The directory level: "root", "directory", or "subdirectory"
 * @returns {string} HTML comment
 */
export function getNavbarHtml(level = 'root') {
  // The navbar is now generated dynamically by navbar.js
  // No need to generate HTML, just return a comment
  return '  <!-- Navigation - Generated by navbar.js -->';
}

// Export for global access
if (typeof window !== 'undefined') {
  window.PersonalPenniesNavbarTemplate = {
    getNavbarHtml
  };
}

console.log('[NavbarTemplate] Module loaded');
